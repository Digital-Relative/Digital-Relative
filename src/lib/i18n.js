// Hook to get translations for current language
export function useTranslation(lang = 'en') {
  const t = TRANSLATIONS[lang] || TRANSLATIONS.en
  return (key) => t[key] || TRANSLATIONS.en[key] || key
}

export function isRTL(lang) {
  return LANGUAGES.find(l => l.code === lang)?.rtl || false
}
